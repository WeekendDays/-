n=0:49;
f1=1000; f2=300; f3=200; 
T1=1/f1; T2=1/f2; T3=1/f3;
A=444.128; a=50*sqrt(2)*pi; o=50*sqrt(2)*pi;
x1=A*exp(-a*n*T1).*sin(o*n*T1);
x2=A*exp(-a*n*T2).*sin(o*n*T2);
x3=A*exp(-a*n*T3).*sin(o*n*T3);
k1=0:99;
k2=0:99;
k3=0:99;
w1=(2*pi/50)*k1;
w2=(2*pi/50)*k2;
w3=(2*pi/50)*k3;
X1=x1*exp(-1i*2*pi/50).^(n'*k1);
X2=x2*exp(-1i*2*pi/50).^(n'*k2);
X3=x3*exp(-1i*2*pi/50).^(n'*k3);
magX1=abs(X1);
magX2=abs(X2);
magX3=abs(X3);
subplot(131);
plot(w1,magX1);grid on;
subplot(132);
plot(w2,magX2);grid on;
subplot(133);
plot(w3,magX3);grid on;

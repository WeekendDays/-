n1=0:15;
n2=0:31;
n3=0:63;
f=64; 
T=1/f;
x41=cos(8*pi*n1*T)+cos(16*pi*n1*T)+cos(20*pi*n1*T);
x42=cos(8*pi*n2*T)+cos(16*pi*n2*T)+cos(20*pi*n2*T);
x43=cos(8*pi*n3*T)+cos(16*pi*n3*T)+cos(20*pi*n3*T);
k1=0:31;
k2=0:63;
k3=0:127;
w1=(2*pi/16)*k1;
w2=(2*pi/32)*k2;
w3=(2*pi/64)*k3;
X41=x41*exp(-1i*2*pi/16).^(n1'*k1);
X42=x42*exp(-1i*2*pi/32).^(n2'*k2);
X43=x43*exp(-1i*2*pi/64).^(n3'*k3);
magX41=abs(X41);
magX42=abs(X42);
magX43=abs(X43);
subplot(221);
plot(w1/pi,magX41);grid on;
title('N=16');
subplot(222);
plot(w2/pi,magX42);grid on;
title('N=32');
subplot(223);
plot(w3/pi,magX43);grid on;
title('N=64');